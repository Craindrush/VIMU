package sample.classes;

public class Puntaje {
    public int puntos;
    public int posicionPuntaje;

    public Puntaje() {
    }

    public Puntaje(int puntos) {
        this.puntos = puntos;
    }

    public Puntaje(int puntos, int posicionPuntaje) {
        this.puntos = puntos;
        this.posicionPuntaje = posicionPuntaje;
    }
}
